# Números para las operaciones aritméticas
a = 10
b = 3

print("--- Operadores Aritméticos ---")
print(f"{a} + {b} = {a + b}")
print(f"{a} - {b} = {a - b}")
print(f"{a} * {b} = {a * b}")
print(f"{a} / {b} = {a / b}")   # división real
print(f"{a} // {b} = {a // b}") # división entera
print(f"{a} % {b} = {a % b}")   # módulo
print(f"{a} ** {b} = {a ** b}") # potencia

# Valores booleanos para operaciones lógicas
x = True
y = False

print("\n--- Operadores Lógicos ---")
print(f"{x} and {y} = {x and y}")
print(f"{x} or {y} = {x or y}")
print(f"not {x} = {not x}")
print(f"not {y} = {not y}")

# Operadores de comparación
print("\n--- Operadores de Comparación ---")
print(f"{a} == {b} es {a == b}")
print(f"{a} != {b} es {a != b}")
print(f"{a} > {b}  es {a > b}")
print(f"{a} < {b}  es {a < b}")
print(f"{a} >= {b} es {a >= b}")
print(f"{a} <= {b} es {a <= b}")